                Jelentkezési útmutató
                    Kedves Kollégák,
           
                    Szeretnénk felhívni figyelmeteket, hogy a sportnapon való részvételnél az egészségi állapototoknak megfelelő aktivitást válasszatok.
            A sportnapon való részvétel regisztrációjával egyben kinyilvánításra kerül, hogy nincs olyan ismert megbetegedésed, amely az általad választott sportágban, az intenzívebb mozgás által, annak következményeként az egészségi állapotodban rosszabbodást okozna.
            
                    A sportbajnokságokra és minden regisztrációhoz kötött programra a jelentkezéseket érkezési sorrendben fogadjuk.
                    A rendezvényre csak egy alkalommal van lehetőség regisztrálni, módosításra nincs lehetőség.
                    Minden munkavállaló maximum 2 sportbajnokságra jelentkezhet - 1 csapatsport és 1 páros sport vagy sakk.
            
                    A csoportos órákra a helyszínen lehet majd jelentkezni, érkezési sorrendben.
            
                    Előfordulhat, hogy a céges levelezési rendszerünk a regisztráció visszaigazolását automatikusan karanténba helyezi, ezért azokat néhány órás késéssel kapjátok meg a karanténoldalon keresztül.
               
           
                                    Focibajnokság:
                                    Minden főcsapat 2 foci csapatot delegálhat a bajnokságba. Kérjük, válasszátok ki a kiscsapatot is a regisztráció során. Javasoljuk, hogy azok a munkavállalók, akik szeretnének egy csapatban lenni, előzetesen egyeztessenek, hogy ugyanabba a kiscsapatba jelentkezzenek. Minimum 6, cserével együtt maximum 10 fő alkot egy csapatot.
                    
                                
                             
                                    Élőcsocsó bajnokság: 
                                    Minden főcsapat 1 csapatot delegálhat a bajnokságba. Kérjük, válasszátok ki a kiscsapatot is a regisztráció során. Javasoljuk, hogy azok a munkavállalók, akik szeretnének egy csapatban lenni, előzetesen egyeztessenek, hogy ugyanabba a kiscsapatba jelentkezzenek. Minimum 5, cserével együtt maximum 7 fő alkot egy csapatot.
                    
                                
                                Ping-pong bajnokság:
                                    Minden főcsapat 8 ping-pong csapatot delegálhat a bajnokságba. Ha szeretnének egy csapatban lenni, előzetesen egyeztessetek és a jelentkezést erre a sportra csak egyikőtök adja le és a szövegdobozba írja be csapattársa nevét és költséghelyét. 2 fő alkot egy csapatot, ettől eltérni nem lehet.
                    
                                
                                Csocsó bajnokság:
                                    Minden főcsapat 6 csocsó csapatot delegálhat a bajnokságba. Ha szeretnének egy csapatban lenni, előzetesen egyeztessetek és a jelentkezést erre a sportra csak egyikőtök adja le, és a szövegdobozba írja be csapattársa nevét és költséghelyét. 2 fő alkot egy csapatot, ettől eltérni nem lehet.
                    
                         
